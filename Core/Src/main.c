/**
  * @brief  The application entry point.
  * @retval int
  */
int main(void)
{
	HAL_Init();
	
	SystemClock_Config();

	MX_GPIO_Init();
	MX_I2C1_Init();

	giro_config(hi2c1);

	double x_g = 0;
	double y_g = 0;
	double z_g = 0;

	uint32_t pin_mask = 0;

	while (1)
	{
		giro_read_angls(hi2c1, &x_g, &y_g, &z_g);

		
		pin_mask = LL_GPIO_ReadOutputPort(GPIOC);
		
		pin_mask = pin_mask & (~(LL_GPIO_PIN_8 | LL_GPIO_PIN_9));

		if(x_g > 0)
		{
			pin_mask = pin_mask | LL_GPIO_PIN_8;
		}
		else if (x_g < 0)
		{
			pin_mask = pin_mask | LL_GPIO_PIN_9;
		} 
		

		LL_GPIO_WriteOutputPort(GPIOC, pin_mask);
	}
}

